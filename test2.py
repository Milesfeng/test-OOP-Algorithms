# -*- coding: utf-8 -*-

# =============================================================================
# 1. 字元「:」改成全型
# =============================================================================

data = '人易科技:上 機 測 驗 - 演算法'
data = data.replace(':','：')

# =============================================================================
# 2. 去掉中文字間的空白(保留-號二側空白) 
# =============================================================================

data = data[:data.find('-')].replace(' ','') + data[data.find('-')-1:]

# =============================================================================
# 3. 列印出符號:到-之間的字元 
# =============================================================================

print(data[data.find('：')+1:data.find('-')])
